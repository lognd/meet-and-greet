"""Question bank for student meetings.

Each meeting receives `questions_per_meeting` randomly chosen questions with at
least one from each half (standard / interesting).
"""

QUESTIONS_STANDARD: list[str] = [
    "What is your major or intended major?",
    "What year are you in?",
    "Where are you from originally?",
    "What is one hobby you have outside of class?",
    "Do you have any pets?",
    "What is your favorite type of music?",
    "Are you more of a morning person or a night owl?",
    "What is a TV show or movie you have been enjoying lately?",
    "Do you prefer coffee, tea, or neither?",
    "What sport (if any) do you follow or play?",
    "Have you declared a minor yet?",
    "What is something you are looking forward to this semester?",
]

QUESTIONS_INTERESTING: list[str] = [
    "If you could have dinner with any historical figure, who would it be and why?",
    "What skill do you wish you had learned much earlier in life?",
    "If you could instantly master one language (spoken or programming), what would it be?",
    "What is the most interesting place you have ever visited?",
    "If you could trade lives with someone for a day, who and why?",
    "What is a belief you hold that most people around you disagree with?",
    "What is the best meal you have ever eaten?",
    "What is a problem you think your generation will have to solve that previous ones didn't?",
    "If money were no object, what career would you pursue?",
    "What is the strangest or most memorable dream you can remember?",
    "What is a book, article, or video that genuinely changed how you see the world?",
    "If you could give your 10-year-old self one piece of advice, what would it be?",
    "What is something most people assume about you that is completely wrong?",
]
